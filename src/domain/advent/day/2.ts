import { map, pipe } from 'rxjs'
import { newAdvent, solveAdvent, solveAdventBonus } from '../command'

export function sumReducer(acc, current) {
  return acc + current
}

const mapping = {
  A: 0,
  B: 1,
  C: 2,
  X: 0,
  Y: 1,
  Z: 2,
}

const opponentMap = ['A', 'B', 'C']

const myMap = ['X', 'Y', 'Z']

const score = [
  [3, 0, 6],
  [6, 3, 0],
  [0, 6, 3],
]

function getScore([opponent, mine]) {
  let currentScore = score[mapping[mine]][mapping[opponent]]
  currentScore += mapping[mine] + 1
  return currentScore
}

function pick([opponent, mine]) {
  let myPick
  if (mine === 'X') {
    myPick = myMap[(opponentMap.indexOf(opponent) + 2) % 3]
  } else if (mine === 'Y') {
    myPick = myMap[(opponentMap.indexOf(opponent) + 0) % 3]
  } else if (mine === 'Z') {
    myPick = myMap[(opponentMap.indexOf(opponent) + 1) % 3]
  }
  return [opponent, myPick]
}

export function day2() {
  newAdvent({ id: '2', input })
  solveAdvent({
    id: '2',
    transform: pipe(
      map((i) => i.input.split(/\n/)), // rounds of strings ['A X']
      map((i) => i.map((i) => i.split(/ /)) as []), // rounds of plays [['A', 'X']]
      map((i) => i.map(getScore)), // round scores [2]
      map((i) => i.reduce(sumReducer)) // sum of rounds 22
    ),
  })
  solveAdventBonus({
    id: '2',
    bonus: pipe(
      map((i) => i.input.split(/\n/)), // rounds of strings ['A X']
      map((i) => i.map((i) => i.split(/ /)) as []), // rounds of plays [['A', 'X']]
      map((i) => i.map<any>(pick)), // round plays with transformed play [['A', 'Z']]
      map((i) => i.map(getScore)), // // round scores [2]
      map((i) => i.reduce(sumReducer)) // // sum of rounds 22
    ),
  })
}

